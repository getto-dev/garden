// Расчёт фазы луны
function getMoonPhase(date: Date): { phase: string; name: string; day: number } {
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  
  // Упрощённый расчёт лунного дня
  const lunarCycle = 29.53058867;
  const knownNewMoon = new Date(2000, 0, 6, 18, 14); // Известное новолуние
  const diff = date.getTime() - knownNewMoon.getTime();
  const days = diff / (1000 * 60 * 60 * 24);
  const lunarDay = ((days % lunarCycle) + lunarCycle) % lunarCycle;
  
  const moonDay = Math.floor(lunarDay) + 1;
  
  if (lunarDay < 1.85) return { phase: 'new', name: '🌑 Новолуние', day: moonDay };
  if (lunarDay < 7.38) return { phase: 'waxing_crescent', name: '🌒 Растущая луна', day: moonDay };
  if (lunarDay < 9.23) return { phase: 'first_quarter', name: '🌓 Первая четверть', day: moonDay };
  if (lunarDay < 14.77) return { phase: 'waxing_gibbous', name: '🌔 Прибывающая луна', day: moonDay };
  if (lunarDay < 16.61) return { phase: 'full', name: '🌕 Полнолуние', day: moonDay };
  if (lunarDay < 22.15) return { phase: 'waning_gibbous', name: '🌖 Убывающая луна', day: moonDay };
  if (lunarDay < 24.00) return { phase: 'last_quarter', name: '🌗 Последняя четверть', day: moonDay };
  return { phase: 'waning_crescent', name: '🌘 Старая луна', day: moonDay };
}

// Знаки зодиака по датам
function getZodiacSign(date: Date): { sign: string; element: string } {
  const month = date.getMonth() + 1;
  const day = date.getDate();
  
  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return { sign: 'Овен', element: 'огонь' };
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return { sign: 'Телец', element: 'земля' };
  if ((month === 5 && day >= 21) || (month === 6 && day <= 20)) return { sign: 'Близнецы', element: 'воздух' };
  if ((month === 6 && day >= 21) || (month === 7 && day <= 22)) return { sign: 'Рак', element: 'вода' };
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return { sign: 'Лев', element: 'огонь' };
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return { sign: 'Дева', element: 'земля' };
  if ((month === 9 && day >= 23) || (month === 10 && day <= 22)) return { sign: 'Весы', element: 'воздух' };
  if ((month === 10 && day >= 23) || (month === 11 && day <= 21)) return { sign: 'Скорпион', element: 'вода' };
  if ((month === 11 && day >= 22) || (month === 12 && day <= 21)) return { sign: 'Стрелец', element: 'огонь' };
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return { sign: 'Козерог', element: 'земля' };
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return { sign: 'Водолей', element: 'воздух' };
  return { sign: 'Рыбы', element: 'вода' };
}

// Определение благоприятности дня
function getDayRecommendations(moonPhase: string, zodiacElement: string): {
  isGoodForSowing: boolean;
  isGoodForPlanting: boolean;
  isGoodForTransplanting: boolean;
  isGoodForWatering: boolean;
  isGoodForFertilizing: boolean;
  isGoodForPruning: boolean;
  isGoodForHarvesting: boolean;
  isGoodForSoilWork: boolean;
  isForbidden: boolean;
  recommendation: string;
} {
  const result = {
    isGoodForSowing: false,
    isGoodForPlanting: false,
    isGoodForTransplanting: false,
    isGoodForWatering: false,
    isGoodForFertilizing: false,
    isGoodForPruning: false,
    isGoodForHarvesting: false,
    isGoodForSoilWork: false,
    isForbidden: false,
    recommendation: ''
  };

  // Новолуние и полнолуние - запрещённые дни
  if (moonPhase === 'new') {
    result.isForbidden = true;
    result.recommendation = 'День отдыха. Не рекомендуется ничего сажать и пересаживать.';
    return result;
  }
  
  if (moonPhase === 'full') {
    result.isForbidden = true;
    result.recommendation = 'Полнолуние. Неблагоприятное время для посева и пересадки. Хорошо для сбора урожая.';
    result.isGoodForHarvesting = true;
    return result;
  }

  // Растущая луна - благоприятно для надземных культур
  if (['waxing_crescent', 'first_quarter', 'waxing_gibbous'].includes(moonPhase)) {
    result.isGoodForSowing = true;
    result.isGoodForPlanting = true;
    result.isGoodForWatering = true;
    result.isGoodForFertilizing = true;
    
    // Знаки земли и воды особенно благоприятны
    if (['земля', 'вода'].includes(zodiacElement)) {
      result.isGoodForTransplanting = true;
      result.recommendation = 'Отличное время для посева и посадки растений, дающих урожай над землёй. Хорошо поливать и удобрять.';
    } else {
      result.recommendation = 'Благоприятное время для посева и посадки. Знак ' + zodiacElement === 'огонь' ? 'огня требует больше воды.' : 'воздуха подходит для цветов.';
    }
  } else {
    // Убывающая луна - благоприятно для корнеплодов
    result.isGoodForTransplanting = true;
    result.isGoodForPruning = true;
    result.isGoodForSoilWork = true;
    result.isGoodForHarvesting = true;
    
    if (['земля', 'вода'].includes(zodiacElement)) {
      result.isGoodForSowing = true;
      result.recommendation = 'Благоприятное время для посадки корнеплодов, луковичных, обрезки и сбора урожая.';
    } else {
      result.recommendation = 'Подходит для обработки почвы, обрезки и сбора урожая.';
    }
  }

  return result;
}

