  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return { sign: 'Овен', element: 'огонь' };
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return { sign: 'Телец', element: 'земля' };
