function getMoonPhase(date: Date): { phase: string; name: string; day: number } {
  const lunarCycle = 29.53058867;
  const knownNewMoon = new Date(2000, 0, 6, 18, 14);
  const diff = date.getTime() - knownNewMoon.getTime();
  const days = diff / (1000 * 60 * 60 * 24);
  const lunarDay = ((days % lunarCycle) + lunarCycle) % lunarCycle;
  
  const moonDay = Math.floor(lunarDay) + 1;
  
  if (lunarDay < 1.85) return { phase: 'new', name: '🌑 Новолуние', day: moonDay };
  if (lunarDay < 7.38) return { phase: 'waxing_crescent', name: '🌒 Растущая луна', day: moonDay };
  if (lunarDay < 9.23) return { phase: 'first_quarter', name: '🌓 Первая четверть', day: moonDay };
  if (lunarDay < 14.77) return { phase: 'waxing_gibbous', name: '🌔 Прибывающая луна', day: moonDay };
  if (lunarDay < 16.61) return { phase: 'full', name: '🌕 Полнолуние', day: moonDay };
  if (lunarDay < 22.15) return { phase: 'waning_gibbous', name: '🌖 Убывающая луна', day: moonDay };
  if (lunarDay < 24.00) return { phase: 'last_quarter', name: '🌗 Последняя четверть', day: moonDay };
  return { phase: 'waning_crescent', name: '🌘 Старая луна', day: moonDay };
}

